/*
  # 集合
  集合是一种不允许重复的数据结构。

  ## 构建数据集合
  集合是由一组无序且不能重复的项所组成的数据结构。集合数据结构使用了和有限集合相同的数学概念。有限集合也就是由有限个元素组成的集合，
  也叫有穷集合。

  在数学里，集合是一组不同对象的集。比如说，一个由大于等于`0`的整数组成的自然集合：`N = {0, 1, 2, 3, 4, 5, 6, ...}`。集合中
  的对象列表用`{}`包围。

  还有一个概念叫做空集。空集就是不包含任何元素的集合。比如24和29之间的素数集合，由于24和29之间没有素数（除了1和自身以外，不能被其他正
  整数整除，就叫做素数），这个集合就是空集。空集用`{}`表示。

  也可以把集合当成一个既没有重复元素，又没有顺序概念的数组。
*/
